    private void jButtonAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAgregarActionPerformed
        String nombrecategoria=jTextFieldCategoria.getText();
        

        if (nombrecategoria.contentEquals("")){
            JOptionPane.showMessageDialog(rootPane,
                "Todos los campos son obligatorios");
        } else {
            try {
                Categoria au=new DAOCategoria().Insertar(nombrecategoria);
                JOptionPane.showMessageDialog(rootPane, "Registro agregado");
            } catch (Exception e) {
                e.printStackTrace();
                JOptionPane.showMessageDialog(rootPane, "No se agrego el registro");
            }
            obtenerDatos();
            limpiarCampos();
        }

    }//GEN-LAST:event_jButtonAgregarActionPerformed

    private void jButtonEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEditarActionPerformed
        jButtonAgregar.setEnabled(false);
        int fila=this.jTableCategoria.getSelectedRow();
        if (fila==-1) {
            JOptionPane.showMessageDialog(rootPane,
                "Seleccione un registro de la tabla");
        } else {
            try {
                int id_catg=Integer.parseInt((String)this.jTableCategoria.getValueAt(fila ,0).toString());
                String nomcatg=(String)this.jTableCategoria.getValueAt(fila, 1);
         

                jTextFieldIDCategoria.setText(""+id_catg);
                jTextFieldCategoria.setText(nomcatg);
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButtonEditarActionPerformed
   
