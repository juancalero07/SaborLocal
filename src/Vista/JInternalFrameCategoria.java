public void obtenerDatos(){
     
    List<Categoria> categorias=new DAOCategoria().obtenerDatos();
    DefaultTableModel modelo= new DefaultTableModel();
    
    String[] columnas={"ID Categoría","Categoría"};
    
  modelo.setColumnIdentifiers(columnas);
  
  for (Categoria au:categorias) {
      
      String[]renglon={Integer.toString(au.getId_catg()), au.getCate_produ()};
      modelo.addRow(renglon);
  }
    jTableCategoria.setModel(modelo);
}
     
public void actualizarCategoria (){
    int id=Integer.parseInt(this.jTextFieldIDCategoria.getText());
    String nombrecategoria=jTextFieldCategoria.getText();
  
    
    DAOCategoria dao=new DAOCategoria();
    int res=dao.Actualizar(id, nombrecategoria);
        if (res==1) {
            JOptionPane.showMessageDialog(rootPane, "Categoria Actualizada");
        } else {
            JOptionPane.showMessageDialog(rootPane, "Ocurrio un ERROR");
        }   
}

public void actualizarInterfazCategorias(List<Categoria> categorias) {
    DefaultTableModel modelo = new DefaultTableModel();

    String[] columnas = {"ID Categoría", "Categoría"};
    modelo.setColumnIdentifiers(columnas);

    for (Categoria categoria : categorias) {
        String[] renglon = {Integer.toString(categoria.getId_catg()),
            categoria.getCate_produ()};
        modelo.addRow(renglon);
    }
    jTableCategoria.setModel(modelo);
}

