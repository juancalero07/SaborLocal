    private void jButtonBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBorrarActionPerformed
    int fila=this.jTableCategoria.getSelectedRow();
    int id_catg=Integer.parseInt((String)this.jTableCategoria.getValueAt(fila, 0).toString());
    DAOCategoria dao=new DAOCategoria();
        
    if (fila==-1) {
    JOptionPane.showMessageDialog(rootPane, "Seleccione un registro de la tabla");
    } else {     
    if (JOptionPane.showConfirmDialog(rootPane, "Se eliminira la categoría, ¿desea continuar?",
"Eliminar Cliente", JOptionPane.WARNING_MESSAGE, JOptionPane.YES_NO_OPTION)
    == JOptionPane.YES_OPTION)
    dao.Eliminar(id_catg);
    obtenerDatos();
    JOptionPane.showMessageDialog(null, "Categoría eliminado correctamente");
        }
    }//GEN-LAST:event_jButtonBorrarActionPerformed

    private boolean busquedaActivaOrden = false;
    
    private void jButtonBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBuscarActionPerformed
    String terminoBusqueda = jTextFieldBuscar.getText().trim();
    if (!terminoBusqueda.isEmpty()) {
        actualizarInterfazCategorias(new DAOCategoria().
                buscarCategorias(terminoBusqueda));
        busquedaActivaOrden = true;
    } else if (busquedaActivaOrden) {
        actualizarInterfazCategorias(new DAOCategoria().
                obtenerDatos());
        busquedaActivaOrden = false;
    } else {
        JOptionPane.showMessageDialog(null,
                "Por favor, ingrese un término de búsqueda.",
                "Advertencia", JOptionPane.WARNING_MESSAGE);
    }
    }//GEN-LAST:event_jButtonBuscarActionPerformed
    
